for i in range(10): #반복조건
    print(i,end=' ')    #반복 조건 참인 동안 반복

print()
for i in range(1,10): #반복조건
    print(i,end=' ')

print()

for k in range(1,11,2): #반복조건
    print(k,end=' ')    #홀수 출력

print()
for a in range(10,0,-1):    #반복조건
    print(a,end=' ')    #역순 출력
